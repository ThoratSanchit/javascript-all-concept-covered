//.at[] is a method primarily associated with arrays and strings in JavaScript,
//introduced as part of ECMAScript 2022 (ES13). This method allows you to access
//elements at a specific index in a more flexible way compared to traditional bracket notation.

let arr = [1, 2, 3, 4, 5, 667, 7, 8];
console.log(arr.at(-1)); //  access element from at the end of the array
console.log(arr[0]); //access the element of the array at the begining of the array
